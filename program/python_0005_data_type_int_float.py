'''
Python有4种数据类型.分别是:
int - 整数
float - 小数
str - 字符串
bool - True / False
'''

# int (integer) - 整数

h = 2 + 3
j = h - 100
k = h * j
print('variable h type is', type(h), 'h=', h)
print('variable j type is', type(j), 'j=', j)
print('variable k type is', type(k), 'k=', k)

# type() is a function.
# what is a function? A function does something for you.
# for example:
# print() is a function. It helps you to print something to the console.
# type() is a function. It tells you the type of some variable.


