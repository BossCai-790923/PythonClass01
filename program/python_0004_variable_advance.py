# define a variable with value = 5
h = 5
print("Now h value is", h)


# 自减操作
# h's old value is 5
# 5 - 1 = 4
# assign 4 back to variable h
# Now h contains a new value 4
h = h - 1
print("after h = h - 1, h","=", h)

h -= 1  # <=> h = h - 1
print("after h -= 1, h","=", h)

h += 1  # <=> h = h + 1
print("after h += 1, h","=", h)

h *= 2  # <=> h = h * 2
print("after h *= 2, h","=", h)

h /= 4  # <=> h = h / 4
print("after h /= 4, h","=", h)



