#Requirment: Implement a plus calculator

# The purpose of "Number a:" is :
# prompt user a message, to tell user what to do.
# So after user see "Number a:", user knows, "Aha! I need to type a number after the message "Number a:"
int_a = int(input("Number a: "))
int_b = int(input("Number b: "))
result = int(int_a) + int(int_b)
print(f"{int_a} + {int_b} = {result}")




